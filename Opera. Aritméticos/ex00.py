# Antecessor e Sucessor de um número
numero = int(input('Digite um valor: '))
ante = numero - 1
suce = numero + 1
print('Analisando o número, o seu antecessor é {} e o seu sucessor é {}'. format(ante, suce))

# Antecede  e sucede 5 vezes
numero2 = int(input('Digite um valor: '))
ante5 = numero2 - 5
suce5 = numero2 + 5
print('Analisando o número, os números que o antecedem e sucedem são {} e {}'. format(ante5, suce5)) 